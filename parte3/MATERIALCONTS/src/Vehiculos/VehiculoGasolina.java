package Vehiculos;

public class VehiculoGasolina extends Vehiculo {

	
	private String contaminacion ;
	private float tamanhoDeposito ;
	
	public String getContaminacion() {
		return contaminacion;
	}
	public void setContaminacion(String contaminacion) {
		this.contaminacion = contaminacion;
	}
	public float getTamanhoDeposito() {
		return tamanhoDeposito;
	}
	public void setTamanhoDeposito(float tamanhoDeposito) {
		this.tamanhoDeposito = tamanhoDeposito;
	}
}
